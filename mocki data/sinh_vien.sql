CREATE PROCEDURE UpdateMaHeDaoTao()
BEGIN
    DECLARE done INT DEFAULT 0;
    DECLARE user_id INT;
    DECLARE he_dao_tao VARCHAR(10);
    DECLARE cur_users CURSOR FOR SELECT ma_nguoi_dung FROM sinh_vien;
    DECLARE cur_programs CURSOR FOR SELECT ma_he_dao_tao FROM (
        SELECT 'CC' AS ma_he_dao_tao UNION ALL
        SELECT 'CQ' UNION ALL
        SELECT 'DTTX' UNION ALL
        SELECT 'QT' UNION ALL
        SELECT 'VHVL' UNION ALL
        SELECT 'VN' UNION ALL
        SELECT 'VP'
    ) t;
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    -- Create temporary table to hold mapping data
    CREATE TEMPORARY TABLE temp_mapping (
        id INT AUTO_INCREMENT PRIMARY KEY,
        ma_he_dao_tao VARCHAR(10)
    );

    INSERT INTO temp_mapping (ma_he_dao_tao)
    SELECT * FROM (
        SELECT 'CC' UNION ALL
        SELECT 'CQ' UNION ALL
        SELECT 'DTTX' UNION ALL
        SELECT 'QT' UNION ALL
        SELECT 'VHVL' UNION ALL
        SELECT 'VN' UNION ALL
        SELECT 'VP'
    ) t;

    OPEN cur_users;
    OPEN cur_programs;

    label_users: LOOP
        FETCH cur_users INTO user_id;
        IF done THEN
            LEAVE label_users;
        END IF;

        -- Fetch program to assign
        SET done = 0;
        FETCH cur_programs INTO he_dao_tao;

        -- If program finishes, start again
        IF done THEN
            SET done = 0;
            CLOSE cur_programs;
            OPEN cur_programs;
            FETCH cur_programs INTO he_dao_tao;
        END IF;

        -- Update user with current program
        UPDATE sinh_vien
        SET ma_he_dao_tao = he_dao_tao
        WHERE ma_nguoi_dung = user_id;

    END LOOP label_users;

    CLOSE cur_users;
    CLOSE cur_programs;

    DROP TEMPORARY TABLE temp_mapping;
END$$

